class Blog {
  final String title;
  final String handle;
  final String blog;
  final double rating;
  final String gender;
  final int likes;
  final int comments;
  final int up;
  final int down;
  Blog({
    required this.title,
    required this.handle,
    required this.blog,
    required this.rating,
    required this.gender,
    required this.likes,
    required this.comments,
    required this.up,
    required this.down,
  });
}
